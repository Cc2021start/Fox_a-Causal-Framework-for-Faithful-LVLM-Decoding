def _stat_1d(name, t):
    t = t.detach()
    finite = torch.isfinite(t)
    n_bad = (~finite).sum().item()
    if finite.any():
        tmin = t[finite].min().item()
        tmax = t[finite].max().item()
    else:
        tmin, tmax = float("nan"), float("nan")
    print(f"[{name}] dtype={t.dtype} finite={finite.float().mean().item():.3f} "
          f"min={tmin:.3e} max={tmax:.3e} n_nonfinite={n_bad}")


